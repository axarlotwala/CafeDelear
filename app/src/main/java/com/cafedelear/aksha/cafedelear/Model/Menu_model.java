package com.cafedelear.aksha.cafedelear.Model;

public class Menu_model {

    private String menu_id;
    private String delear_id;
    private String cat_id;
    private String menu_name;
    private String menu_url;
    private String menu_price;
    private String is_visible;
    private String menu_creat_time;

    public Menu_model() {
    }

    public String getMenu_id() {
        return menu_id;
    }

    public void setMenu_id(String menu_id) {
        this.menu_id = menu_id;
    }

    public String getDelear_id() {
        return delear_id;
    }

    public void setDelear_id(String delear_id) {
        this.delear_id = delear_id;
    }

    public String getCat_id() {
        return cat_id;
    }

    public void setCat_id(String cat_id) {
        this.cat_id = cat_id;
    }

    public String getMenu_name() {
        return menu_name;
    }

    public void setMenu_name(String menu_name) {
        this.menu_name = menu_name;
    }

    public String getMenu_url() {
        return menu_url;
    }

    public void setMenu_url(String menu_url) {
        this.menu_url = menu_url;
    }

    public String getMenu_price() {
        return menu_price;
    }

    public void setMenu_price(String menu_price) {
        this.menu_price = menu_price;
    }

    public String getIs_visible() {
        return is_visible;
    }

    public void setIs_visible(String is_visible) {
        this.is_visible = is_visible;
    }

    public String getMenu_creat_time() {
        return menu_creat_time;
    }

    public void setMenu_creat_time(String menu_creat_time) {
        this.menu_creat_time = menu_creat_time;
    }
}
